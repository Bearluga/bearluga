							<p>
							&nbsp;&nbsp;&nbsp;&nbsp; 
							2007年4月24日，天慕众和应合作伙伴--<a target="_blank" href="http://www.bj-hr.com.cn/">北京市东城人才服务中心</a>的邀请，协助东城人才服务中心对于应聘北京某机关国家公务员的16名应聘者进行了性格因素倾向性测试。测试采用了权威性的卡特尔16PF性格因素测试案，这种测试方法是美国心理学家卡特尔经过大量实际案例的<img border="0" src="photos/16PF%20report.gif" width="181" height="231" align="left">科学分析归类，把人类的行为的1800种表现描述归纳为16种根源因素特质，用于呈现人类潜在的、稳定的人格特征，具有实质性的表达效果。这16种性格因素在每个个体人类身上的组合，构成了每个人独特的人格特征。<br>
&nbsp;&nbsp;&nbsp; 卡特尔16PF测试是一种管理测试手段，适用于人才选拔、发展预测的性格因素判断，对于企业用人和个人职业规划，都有很科学的剖析参考价值。这次测试，是<a target="_blank" href="http://www.bj-hr.com.cn/">东城人才服务中心</a>对该机关招聘的国家公务员进行考试、考察的一部分内容，除了素质、技能、专业考试以外，这是首次加入了性格因素测试，这个测试也是该机关特别提出的要求，希望能够对于应聘国家公务员的人员性格倾向有所了解，以参考衡量是否适合于岗位工作特点。测试结果以每人独立报告形式反馈提供给用人单位，报告中包含了16因素倾向性描述以及综合分析，报告反馈后引起了用人单位极大兴趣和重视，为人选的确认起到了量化评估的参考作用。<br>
&nbsp;&nbsp;&nbsp; 类似这样的评估测试，目前在国内各类招聘、择优中应用并不普遍，但是以科学的手段来量化评估、考察、管理人员，已经是国际化的趋势。天慕众和目前结合中国实际人力资源条件和状况，可以使用类似的多种循证性质的评估测试，目前仍在积极与美国某著名心理研究机构合作，以应用更为多样化的、科学量化适用于不同领域和层次人员的职业倾向、性格特征、心理素质等方面的测评。<br>
